  void addNewConnection(String friendEmail) async {
    bool flagNewConnection = false;
    var chat_id;
    String date = DateTime.now().toIso8601String();
    CollectionReference chats = firestore.collection("chats");
    CollectionReference users = firestore.collection("lawyer");

    final docChats =
        await users.doc(_currentUser!.email).collection("lawyer").get();

    if (docChats.docs.length != 0) {
      final checkConnection = await users
          .doc(_currentUser!.email)
          .collection("chats")
          .where("connection", isEqualTo: friendEmail)
          .get();

      if (checkConnection.docs.length != 0) {
        flagNewConnection = false;
        chat_id = checkConnection.docs[0].id;
      } else {
        flagNewConnection = true;
      }
    } else {
      flagNewConnection = true;
    }

    if (flagNewConnection) {
      // cek dari chats collection => connections => mereka berdua...
      final chatsDocs = await chats.where(
        "connections",
        whereIn: [
          [
            _currentUser!.email,
            friendEmail,
          ],
          [
            friendEmail,
            _currentUser!.email,
          ],
        ],
      ).get();

      if (chatsDocs.docs.length != 0) {
        final chatDataId = chatsDocs.docs[0].id;
        final chatsData = chatsDocs.docs[0].data() as Map<String, dynamic>;

        await users
            .doc(_currentUser!.email)
            .collection("chats")
            .doc(chatDataId)
            .set({
          "connection": friendEmail,
          "lastTime": chatsData["lastTime"],
          "total_unread": 0,
        });

        final listChats =
            await users.doc(_currentUser!.email).collection("chats").get();

        if (listChats.docs.length != 0) {
          List<ChatUser> dataListChats = List<ChatUser>.empty();
          listChats.docs.forEach((element) {
            var dataDocChat = element.data();
            var dataDocChatId = element.id;
            dataListChats.add(ChatUser(
              chatId: dataDocChatId,
              connection: dataDocChat["connection"],
              lastTime: dataDocChat["lastTime"],
              total_unread: dataDocChat["total_unread"],
            ));
          });
          user.update((user) {
            user!.chats = dataListChats;
          });
        } else {
          user.update((user) {
            user!.chats = [];
          });
        }

        chat_id = chatDataId;

        user.refresh();
      } else {
        final newChatDoc = await chats.add({
          "connections": [
            _currentUser!.email,
            friendEmail,
          ],
        });
        await chats.doc(newChatDoc.id).collection("chat");

        await users
            .doc(_currentUser!.email)
            .collection("chats")
            .doc(newChatDoc.id)
            .set({
          "connection": friendEmail,
          "lastTime": date,
          "total_unread": 0,
        });

        final listChats =
            await users.doc(_currentUser!.email).collection("chats").get();

        if (listChats.docs.length != 0) {
          List<ChatUser> dataListChats = List<ChatUser>.empty();
          listChats.docs.forEach((element) {
            var dataDocChat = element.data();
            var dataDocChatId = element.id;
            dataListChats.add(ChatUser(
              chatId: dataDocChatId,
              connection: dataDocChat["connection"],
              lastTime: dataDocChat["lastTime"],
              total_unread: dataDocChat["total_unread"],
            ));
          });
          user.update((user) {
            user!.chats = dataListChats;
          });
        } else {
          user.update((user) {
            user!.chats = [];
          });
        }

        chat_id = newChatDoc.id;

        user.refresh();
      }
    }

    print(chat_id);

    final updateStatusChat = await chats
        .doc(chat_id)
        .collection("chat")
        .where("isRead", isEqualTo: false)
        .where("penerima", isEqualTo: _currentUser!.email)
        .get();

    updateStatusChat.docs.forEach((element) async {
      await chats
          .doc(chat_id)
          .collection("chat")
          .doc(element.id)
          .update({"isRead": true});
    });

    await users
        .doc(_currentUser!.email)
        .collection("chats")
        .doc(chat_id)
        .update({"total_unread": 0});

    Get.to(
      Chat_room_vieww(),
      arguments: {
        "chat_id": "$chat_id",
        "friendEmail": friendEmail,
      },
    );
  }
  void SignInwithEmail({
    required String name,
    required String email,
    required String password,
  }) async {
    try {
      await auth
          .createUserWithEmailAndPassword(
        email: email,
        password: password,
      )
          .then((value) {
        disblleusername = name;
        auth.currentUser!.updateDisplayName(name);
      });
      update();
      Get.offAllNamed(Routes.HOOME);

      // ignore: nullable_type_in_catch_clause
    } on FirebaseAuthException catch (Errore) {
      String title = Errore.code.replaceAll(RegExp('-'), ' ').capitalize!;
      String massage = '';
      if (Errore.code == 'weak-password') {
        massage = "The password provided is too weak";
      } else if (Errore.code == 'email-already-in-use') {
        massage = "The account already exists for that email";
      } else {
        massage = Errore.message.toString();
      }
      Get.snackbar(title, massage,
          snackPosition: SnackPosition.BOTTOM,
          backgroundColor: Color(0xffD59042),
          colorText: Colors.white);
    } catch (error) {
      Get.snackbar('Errore', error.toString(),
          snackPosition: SnackPosition.BOTTOM,
          backgroundColor: Color(0xffD59042),
          colorText: Colors.white);
    }
  }
  void ResetPassword({required String email}) async {
    try {
      await auth.sendPasswordResetEmail(email: email);
      update();
      Get.back();
      ;
    } on FirebaseAuthException catch (e) {
      String title = e.code.replaceAll(RegExp('-'), ' ').capitalize!;
      String massage = '';
      if (e.code == 'user-not-found') {
        massage =
        ' Account does not exists for that $email.. Create your account by signing up..';
      } else {
        massage = e.message.toString();
      }
      Get.snackbar(title, massage,
          snackPosition: SnackPosition.BOTTOM,
          backgroundColor: Color(0xffD59042),
          colorText: Colors.white);
    } catch (error) {
      Get.snackbar('Errore', error.toString(),
          snackPosition: SnackPosition.BOTTOM,
          backgroundColor: Color(0xffD59042),
          colorText: Colors.white);
    }
  }
  void SignUpUser({
    required String email,
    required String password,
  }) async {
    try {
      await auth
          .signInWithEmailAndPassword(email: email, password: password)
          .then((value) => disblleusername = auth.currentUser!.displayName!);
      update();
      Get.offAllNamed(Routes.HOOME);
    } on FirebaseAuthException catch (e) {
      String title = e.code.replaceAll(RegExp('-'), ' ').capitalize!;
      String massage = '';
      if (e.code == 'user-not-found') {
        massage =
        ' Account does not exists for that $email.. Create your account by signing up..';
        print(e);
      } else if (e.code == 'wrong-password') {
        massage = ' Invalid Password... PLease try again! ';
        print(e);
      } else {
        massage = e.message.toString();
      }
      Get.snackbar(title, massage,
          snackPosition: SnackPosition.BOTTOM,
          backgroundColor: Color(0xffD59042),
          colorText: Colors.white);
    } catch (error) {
      Get.snackbar('Errore', error.toString(),
          snackPosition: SnackPosition.BOTTOM,
          backgroundColor: Color(0xffD59042),
          colorText: Colors.white);
      print(error);
    }
  }
